// 画像パスヘルパー
export const getAnimalImagePath = (animal: AnimalName | string, type: 'full' | 'face' = 'full'): string => {
  // 正規化: 別表記を正式表記に変換
  const normalizedAnimal = animalNameNormalizeMap[animal] || animal as AnimalName;
  const fileName = animalImageMap[normalizedAnimal];
  if (!fileName) {
    console.warn(`Unknown animal: ${animal}`);
    return `/images/animals/${type}/unknown.png`;
  }
  return `/images/animals/${type}/${fileName}.png`;
};
